package org.toobsframework.pres.chart;

import org.toobsframework.exception.BaseException;

public class ChartInitializationException extends BaseException {

  private static final long serialVersionUID = 1L;

  public ChartInitializationException() {
    super();
  }

  public ChartInitializationException(String message, Throwable cause) {
    super(message, cause);
  }

  public ChartInitializationException(String message) {
    super(message);
  }

  public ChartInitializationException(Throwable cause) {
    super(cause);
  }

}
